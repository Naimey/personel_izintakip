    private void izinTablosuGuncelle(String sicilNo) {
        
        try {
            
            Statement s = baglantiAc();
            String sql = "SELECT i.* FROM personel kb INNER JOIN izin i ON "
                    + "kb.sicil_no = i.sicil_no WHERE kb.sicil_no = " + sicilNo;
            
            ResultSet rs = s.executeQuery(sql);
            
            PersonelIzinTabloModelim model = new PersonelIzinTabloModelim(rs);
            personelIzinTablom.setModel(model);
            
            baglantiKapat();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tablo Gösterilirken Hata Oluştu!!!\n" + e);
        }
    }
    
    private void personelTaplomIzinMiktarıTablosunuGoster() {
        
        try {
            
            Statement s = baglantiAc();
            String sql = "SELECT kb.ad, kb.soyad, sum(i.kullanılan_izin_mik) "
                    + "FROM personel kb INNER JOIN izin i ON kb.sicil_no "
                    + "= i.sicil_no GROUP BY kb.ad";
            
            ResultSet rs = s.executeQuery(sql);
            
            KullanilanIzinMikTabloModelim model = new KullanilanIzinMikTabloModelim(rs);
            toplamIzinMiktariTablom.setModel(model);
            
            baglantiKapat();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Personel Toplam İzin Miktarını Gösterirken Hata Oluştu!!!\n" + e);
        }
    }
    
    private void tabloAramaYap(String secilenTarih) {
        
        try {
            
            Statement s = baglantiAc();
            String sql = "SELECT kb.ad, kb.soyad, i.izin_bts_tarih FROM "
                    + "personel kb INNER JOIN izin i ON kb.sicil_no"
                    + " = i.sicil_no WHERE DATE(i.izin_bas_tarih) <= '" + 
                    secilenTarih + "' AND DATE(i.izin_bts_tarih) >= '" +
                    secilenTarih + "' ";
            ResultSet rs = s.executeQuery(sql);
            
            TarihGoreAramaTableModel model = new TarihGoreAramaTableModel(rs);
            dateChooserTablom.setModel(model);
            
            baglantiKapat();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tarih Göre Tablo Aranırken Hata Oluştu!!!\n" + e);
        }
    }
    
    
