    baglanti vb = new baglanti();
    
    public Statement baglantiAc() throws SQLException {
        
        try {
            Class.forName(vb.DRIVER).newInstance();
            vb.con = DriverManager.getConnection(vb.URL + vb.DB_NAME, vb.ID, vb.PW);
        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Veritabanı Bağlantısı Hatası!!!\n" + e);
        }
        return vb.con.createStatement();
    }
    
    public void baglantiKapat() throws SQLException {
        vb.con.close();
    }
    
    private void personelSicilNoComboboxIcerikDuzenleme() {
        
        try {
            
            Statement s = baglantiAc();
            
            String sql = "SELECT sicil_no FROM personel";
            ResultSet rs = s.executeQuery(sql);
            
            while(rs.next()) {
                personelCombobox.addItem(rs.getObject("sicil_no"));
            }
            baglantiKapat();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Sicil No Bulunurken Hata Meydana Geldi!!!\n" + e);
        }
    }
    
    private void personelTablosunuAyarla() {
        
        try {
            
            Statement s = baglantiAc();
            String sql = "SELECT * FROM personel";
            ResultSet rs = s.executeQuery(sql);
            
            PersonelTabloModelim model = new PersonelTabloModelim(rs);
            personelTablom.setModel(model);
            
            baglantiKapat();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Personel Tablosu Gösterilirken Hata Oluştu!!!\n" + e);
        }
    }
    
    
